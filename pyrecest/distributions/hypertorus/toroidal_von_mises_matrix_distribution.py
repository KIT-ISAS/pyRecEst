    def pdf(self, xs):
        assert xs.shape[-1] == 2
        xs = reshape(xs, (-1, self.input_dim))
        exponent = (
            self.kappa[0] * cos(xs[:, 0] - self.mu[0]) +
            self.kappa[1] * cos(xs[:, 1] - self.mu[1]) +
            cos(xs[:, 0] - self.mu[0]) * self.A[0, 0] * cos(xs[:, 1] - self.mu[1]) +
            cos(xs[:, 0] - self.mu[0]) * self.A[0, 1] * sin(xs[:, 1] - self.mu[1]) +
            sin(xs[:, 0] - self.mu[0]) * self.A[1, 0] * cos(xs[:, 1] - self.mu[1]) +
            sin(xs[:, 0] - self.mu[0]) * self.A[1, 1] * sin(xs[:, 1] - self.mu[1])
        )
        return self.C * exp(exponent)

    def log_likelihood(self, samples):
        """
        Calculates the log-likelihood of the given samples.

        Parameters:
            samples (n x 2 array): one sample per row

        Returns:
            l (scalar): log-likelihood of given samples
        """
        assert samples.shape[-1] == 2
        samples = reshape(samples, (-1, 2))
        exponent = (
            self.kappa[0] * cos(samples[:, 0] - self.mu[0]) +
            self.kappa[1] * cos(samples[:, 1] - self.mu[1]) +
            cos(samples[:, 0] - self.mu[0]) * self.A[0, 0] * cos(samples[:, 1] - self.mu[1]) +
            cos(samples[:, 0] - self.mu[0]) * self.A[0, 1] * sin(samples[:, 1] - self.mu[1]) +
            sin(samples[:, 0] - self.mu[0]) * self.A[1, 0] * cos(samples[:, 1] - self.mu[1]) +
            sin(samples[:, 0] - self.mu[0]) * self.A[1, 1] * sin(samples[:, 1] - self.mu[1])
        )
        return len(samples) * log(self.C) + sum(exponent)

    def log_likelihood_diff(self, samples):
        """
        Calculates derivatives of log-likelihood using analytical derivatives.

        Parameters:
            samples (n x 2 array): one sample per row

        Returns:
            ld (1 x 8 array): derivative of log-likelihood w.r.t.
                mu1, mu2, kappa1, kappa2, a11, a12, a21, a22

        Gerhard Kurz, Uwe D. Hanebeck,
        Toroidal Information Fusion Based on the Bivariate von Mises Distribution
        Proceedings of the 2015 IEEE International Conference on Multisensor
        Fusion and Information Integration (MFI 2015),
        San Diego, California, USA, September 2015.
        """
        assert samples.shape[-1] == 2
        samples = reshape(samples, (-1, 2))
        cinv_diff = self.norm_const_approx_diff()
        n = len(samples)

        ld = zeros(8)

        # derivative w.r.t. mu1
        ld[0] = sum(
            self.kappa[0] * sin(samples[:, 0] - self.mu[0]) +
            sin(samples[:, 0] - self.mu[0]) * self.A[0, 0] * cos(samples[:, 1] - self.mu[1]) +
            sin(samples[:, 0] - self.mu[0]) * self.A[0, 1] * sin(samples[:, 1] - self.mu[1]) -
            cos(samples[:, 0] - self.mu[0]) * self.A[1, 0] * cos(samples[:, 1] - self.mu[1]) -
            cos(samples[:, 0] - self.mu[0]) * self.A[1, 1] * sin(samples[:, 1] - self.mu[1])
        )

        # derivative w.r.t. mu2
        ld[1] = sum(
            self.kappa[1] * sin(samples[:, 1] - self.mu[1]) +
            cos(samples[:, 0] - self.mu[0]) * self.A[0, 0] * sin(samples[:, 1] - self.mu[1]) -
            cos(samples[:, 0] - self.mu[0]) * self.A[0, 1] * cos(samples[:, 1] - self.mu[1]) +
            sin(samples[:, 0] - self.mu[0]) * self.A[1, 0] * sin(samples[:, 1] - self.mu[1]) -
            sin(samples[:, 0] - self.mu[0]) * self.A[1, 1] * cos(samples[:, 1] - self.mu[1])
        )

        # derivative w.r.t. kappa1
        ld[2] = -n * self.C * cinv_diff[0] + sum(cos(samples[:, 0] - self.mu[0]))

        # derivative w.r.t. kappa2
        ld[3] = -n * self.C * cinv_diff[1] + sum(cos(samples[:, 1] - self.mu[1]))

        # derivative w.r.t. a11
        ld[4] = -n * self.C * cinv_diff[2] + sum(cos(samples[:, 0] - self.mu[0]) * cos(samples[:, 1] - self.mu[1]))

        # derivative w.r.t. a12
        ld[5] = -n * self.C * cinv_diff[3] + sum(cos(samples[:, 0] - self.mu[0]) * sin(samples[:, 1] - self.mu[1]))

        # derivative w.r.t. a21
        ld[6] = -n * self.C * cinv_diff[4] + sum(sin(samples[:, 0] - self.mu[0]) * cos(samples[:, 1] - self.mu[1]))

        # derivative w.r.t. a22
        ld[7] = -n * self.C * cinv_diff[5] + sum(sin(samples[:, 0] - self.mu[0]) * sin(samples[:, 1] - self.mu[1]))

        return ld

    def multiply(self, tvm2):
        """
        Multiply two bivariate von Mises distributions (exact).

        Parameters:
            tvm2 (ToroidalVonMisesMatrixDistribution): distribution to multiply with

        Returns:
            tvm (ToroidalVonMisesMatrixDistribution): renormalized product

        Gerhard Kurz, Uwe D. Hanebeck,
        Toroidal Information Fusion Based on the Bivariate von Mises Distribution
        Proceedings of the 2015 IEEE International Conference on Multisensor
        Fusion and Information Integration (MFI 2015),
        San Diego, California, USA, September 2015.
        """
        assert isinstance(tvm2, ToroidalVonMisesMatrixDistribution)

        C1 = self.kappa[0] * cos(self.mu[0]) + tvm2.kappa[0] * cos(tvm2.mu[0])
        S1 = self.kappa[0] * sin(self.mu[0]) + tvm2.kappa[0] * sin(tvm2.mu[0])
        C2 = self.kappa[1] * cos(self.mu[1]) + tvm2.kappa[1] * cos(tvm2.mu[1])
        S2 = self.kappa[1] * sin(self.mu[1]) + tvm2.kappa[1] * sin(tvm2.mu[1])
        mu_ = array([mod(arctan2(S1, C1), 2 * pi), mod(arctan2(S2, C2), 2 * pi)])
        kappa_ = array([sqrt(C1 ** 2 + S1 ** 2), sqrt(C2 ** 2 + S2 ** 2)])

        def M(mu):
            return array([
                [cos(mu[0]) * cos(mu[1]), -sin(mu[0]) * cos(mu[1]), -cos(mu[0]) * sin(mu[1]),  sin(mu[0]) * sin(mu[1])],
                [sin(mu[0]) * cos(mu[1]),  cos(mu[0]) * cos(mu[1]), -sin(mu[0]) * sin(mu[1]), -cos(mu[0]) * sin(mu[1])],
                [cos(mu[0]) * sin(mu[1]), -sin(mu[0]) * sin(mu[1]),  cos(mu[0]) * cos(mu[1]), -sin(mu[0]) * cos(mu[1])],
                [sin(mu[0]) * sin(mu[1]),  cos(mu[0]) * sin(mu[1]),  sin(mu[0]) * cos(mu[1]),  cos(mu[0]) * cos(mu[1])]
            ])

        b = M(self.mu) @ array([self.A[0, 0], self.A[1, 0], self.A[0, 1], self.A[1, 1]]) + M(tvm2.mu) @ array([tvm2.A[0, 0], tvm2.A[1, 0], tvm2.A[0, 1], tvm2.A[1, 1]])
        a = linalg.solve(M(mu_), b)
        A_ = array([[a[0], a[2]], [a[1], a[3]]])

        return ToroidalVonMisesMatrixDistribution(mu_, kappa_, A_)

    def marginalize_to_1D(self, dimension):
        """
        Get marginal distribution in the given dimension (1 or 2).

        Parameters:
            dimension (int): 1 or 2, the dimension to keep (the other is marginalized out)

        Returns:
            dist (CustomCircularDistribution): marginal distribution
        """
        assert dimension in (1, 2)

        other = 3 - dimension

        def alpha(x):
            return (self.kappa[other - 1] +
                    cos(x - self.mu[dimension - 1]) * self.A[0, 0] +
                    sin(x - self.mu[dimension - 1]) * self.A[1, 0])

        def beta(x):
            return (sin(x - self.mu[dimension - 1]) * self.A[1, 1] +
                    cos(x - self.mu[dimension - 1]) * self.A[0, 1])

        def f(x):
            return (2 * pi * self.C *
                    iv(0, sqrt(alpha(x) ** 2 + beta(x) ** 2)) *
                    exp(self.kappa[dimension - 1] * cos(x - self.mu[dimension - 1])))

        return CustomCircularDistribution(f)

    def shift(self, shift_by):
        """
        Shift distribution by the given angles.

        Parameters:
            shift_by: array of shape (dim,) or (dim, 1)

        Returns:
            tvm (ToroidalVonMisesMatrixDistribution): shifted distribution
        """
        shift_by = reshape(shift_by, (-1,))
        assert shift_by.shape == (self.dim,)
        tvm = ToroidalVonMisesMatrixDistribution(self.mu, self.kappa, self.A)
        tvm.mu = mod(self.mu + shift_by, 2 * pi)
        return tvm

    @staticmethod
    def mle_numerical(samples, method="nelder-mead"):
        """
        Fit distribution to samples using maximum likelihood estimation.

        Parameters:
            samples (n x 2 array): one sample per row
            method (str): optimization method ("nelder-mead", "L-BFGS-B", "trust-constr")

        Returns:
            tvm (ToroidalVonMisesMatrixDistribution): fitted distribution
        """
        assert samples.shape[-1] == 2
        samples = reshape(samples, (-1, 2))

        def neg_log_likelihood(x):
            mu_temp = array(x[:2])
            kappa_temp = array(x[2:4])
            A_temp = array(x[4:8]).reshape(2, 2)
            if all(kappa_temp > 0):
                dist = ToroidalVonMisesMatrixDistribution(mu_temp, kappa_temp, A_temp)
                return -dist.log_likelihood(samples)
            return float('inf')

        # start value: use circular mean direction and approximate kappa
        mean_sin = mean(sin(samples), axis=0)
        mean_cos = mean(cos(samples), axis=0)
        mean_dir = arctan2(mean_sin, mean_cos)
        mean_cos_centered = mean(cos(samples - mean_dir[None, :]), axis=0)
        kappa_start = clip(-log(clip(mean_cos_centered, 1e-10, 1 - 1e-10)), 0.01, 10.0)
        start_value = concatenate([mean_dir, kappa_start, zeros(4)])

        result = scipy.optimize.minimize(neg_log_likelihood, start_value, method=method,
                                         options={"xatol": 1e-6, "fatol": 1e-6, "maxiter": 10000})
        x = result.x
        return ToroidalVonMisesMatrixDistribution(array(x[:2]), array(x[2:4]), array(x[4:8]).reshape(2, 2))
