    def __init__(self, mu, kappa, A):
        AbstractToroidalDistribution.__init__(self)
        assert mu.shape == (2,)
        assert kappa.shape == (2,)
        assert A.shape == (2, 2)
        assert kappa[0] > 0
        assert kappa[1] > 0
        self.mu = mod(mu, 2 * pi)
        self.kappa = kappa
        self.A = A

        if self.kappa[0] > 1.5 or self.kappa[1] > 1.5 or amax(abs(self.A)) > 1:
            self.C = 1.0
            self.C = 1.0 / self.integrate()
        else:
            self.C = self.norm_const_approx()

